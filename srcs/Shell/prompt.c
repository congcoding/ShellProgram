#include "minishell.h"

int		prompt(char **line)
{
	int flag;

	ft_write(1, "$> ");
	if ((flag = get_next_line(0, line)) == 0)
	{
		ft_write_n(1, "exit");
		ft_exit();
	}
	return (0);
}
